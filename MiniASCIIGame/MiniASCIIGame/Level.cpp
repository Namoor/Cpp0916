void Level::DrawLevel(int PlayerXPos, int PlayerYPos)
{
	// Level Zeichnen

	for (int y = 0; y < 20; y++)
	{
		for (int x = 0; x < 30; x++)
		{
			if (PlayerXPos == x && PlayerYPos == y)
			{
				cout << 'X';
			}
			else
			{
				cout << GetBackground(x, y);
			}
		}
		cout << '\n';
	}

	int asd = 0;
}


char Level::GetBackground(int x, int y)
{
	return m_pLevelData[y * 30 + x];
}


void Level::SetBackground(int x, int y, char value)
{
	m_pLevelData[y * 30 + x] = value;
}